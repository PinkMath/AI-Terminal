def _typing_indicator(stop_event):
    spinner = ["â ‹","â ™","â ¹","â ¸","â ¼","â ´","â ¦","â §","â ‡","â "]
    i = 0
    while not stop_event.is_set():
        sys.stdout.write(f"\r{C['ai']}{spinner[i % len(spinner)]}{C['reset']} Thinking...")
        sys.stdout.flush()
        time.sleep(0.08)
        i += 1
    sys.stdout.write("\r" + " " * 30 + "\r")

